package com.example.bookreader.po.Audio;
public class Audio {
    private int id;
    private String radioUrl;
    private int audioNo;
    private String audioName;
    private int bookId;
    private String audioDescription;

    public int getId() { return id; }

    public void setId(int id) { this.id = id; }

    public String getRadioUrl() { return radioUrl; }

    public void setRadioUrl(String radioUrl) { this.radioUrl = radioUrl; }

    public int getAudioNo() { return audioNo; }

    public void setAudioNo(int audioNo) { this.audioNo = audioNo; }

    public String getAudioName() { return audioName; }

    public void setAudioName(String audioName) { this.audioName = audioName; }

    public int getBookId() { return bookId; }

    public void setBookId(int bookId) { this.bookId = bookId; }

    public String getAudioDescription() { return audioDescription; }

    public void setAudioDescription(String audioDescription) { this.audioDescription = audioDescription; }

}
